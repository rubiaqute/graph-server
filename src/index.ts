import { ApolloServer, gql } from "apollo-server";
const typeDefs = gql`
  type Book {
    title: String
    author: String
  }

  type Query {
    books: [Book]
  }
`;

const resolvers = {
  Query: {
    books: () => books,
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
  cache: "bounded",
});
server.listen().then(({ url }) => {
  console.log(`ðŸš€  Server ready at ${url}`);
});
